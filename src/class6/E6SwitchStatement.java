package class6;

public class E6SwitchStatement {
    public static void main(String[] args) {
        String name="Jane";

        switch(name){
            case "Jane":
                System.out.println("Mexico");
                break;
            case "Mike":
                System.out.println("London");
                break;
            case "Bill":
                System.out.println("New york");
        }
    }
}
