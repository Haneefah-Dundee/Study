package class3;

public class E4Division {
    public static void main(String[] args) {
        System.out.println(10/2);
        System.out.println(10/3);//Answer3... int cannot hold decimal
        System.out.println(11.0/5.0);

        int num1=10;
        int num2=4;
        System.out.println(num1/num2);// Answer 2....int cannot show decimal

        double number1=10;
        double number2=4;
        System.out.println(number1/number2); //Answer 2.5
    }
}
