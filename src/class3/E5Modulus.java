package class3;

public class E5Modulus {
    public static void main(String[] args) {

        int number1=10;
        int number2=3;
        System.out.println(number1/number2);
        System.out.println(number1%number2);

        int noOfSlices=5;
        int noOfPeople=3;
        System.out.println(noOfSlices/noOfPeople);
        System.out.println(noOfSlices%noOfPeople);
    }
}
