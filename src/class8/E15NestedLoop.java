package class8;

public class E15NestedLoop {
    public static void main(String[] args) {
        /*
        print 1 1
              1 2
              1 3
              2 1
              2 2
              2 3
         */


        for (int m = 1; m <3; m++) {
            for (int j = 1; j <4; j++) {
                System.out.println(m+" "+j);
            }

        }
        }
    }