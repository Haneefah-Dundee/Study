package class8;

public class E10NestedForLoop {
    public static void main(String[] args) {
        /*print # # # # #
                # # # # #
                # # # # #
                # # # # #
                # # # # #
                with nested loops*/


        for (int k = 0; k < 5; k++) {
            for (int i = 0; i < 5; i++) {
                System.out.print("# ");
            }
            System.out.println();

        }


    }
}
