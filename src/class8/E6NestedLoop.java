package class8;

public class E6NestedLoop {
    public static void main(String[] args) {
//if you want to repeat a loop, writing the same thing multiple times is not good
        for (int i = 0; i < 5; i++) {
            System.out.print(i+" ");
        }
        System.out.println();

        for (int i = 0; i < 5; i++) {
            System.out.print(i+" ");

        }
        System.out.println();

        for (int i = 0; i < 5; i++) {
            System.out.print(i+" ");

        }
    }
}
